/**
 * Represents a day of the week.
 */
export enum DayOfWeek {
  /**
   * The day is Sunday.
   */
  SUNDAY = 1,
  /**
   * The day is Monday.
   */
  MONDAY = 2,
  /**
   * The day is Tuesday.
   */
  TUESDAY = 3,
  /**
   * The day is Wednesday.
   */
  WEDNESDAY = 4,
  /**
   * The day is Thursday.
   */
  THURSDAY = 5,
  /**
   * The day is Friday.
   */
  FRIDAY = 6,
  /**
   * The day is Saturday.
   */
  SATURDAY = 7,
}
